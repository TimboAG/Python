#para eliminar datos de lista
lista=["abracadabra", "hola", "mundo", "hola", "tu", "vos" ]
print(lista)

#para eliminar un dato especifico por index
del lista[0]
print(lista)

#para eliminar desde hasta
del lista[:2]
print(lista)

#para los diccionarios tmb
dic={"abracadabra" : 1, "hola" : 2, "mundo" :3, "hola" : 4, "tu" : 5, "vos" : 6 }
del dic["hola"]
print(dic)